        <h3 class="h2">
          <strong class="bold-text-3">The </strong>
          <strong class="bold-text-9">design</strong>
        </h3>
      </div>

      <div class="columns-47 w-row">
        <div class="column-103 w-col w-col-6">
          <div class="div-block-93">
            <p>
              When designing for a neurodivergent audience, it&#x27;s important to find the right balance between text
              and images. And if you&#x27;re not neuroatypical but it still resonates with you, that&#x27;s perfectly
              normal. We sometimes forget that inclusive and accessible design benefits everyone. A <a
                href="https://journals.sagepub.com/doi/10.1177/0956797617694867?cookieSet=1" target="_blank"
                class="paragraph-link">study conducted by the Association for Psychological Science</a> found that the
              balance between text and images helps create a structure that many neurodivergent people need to get their
              bearings. You can also improve this structure by using lists with a different icon on each line which is
              easier to navigate than bullet points.<br /><br />If you&#x27;re a designer, you know the importance of
              colour to process information. It&#x27;s even more impactful for neuroatypical individuals. When choosing
              colours, <a href="https://nightingaledvs.com/designing-for-neurodivergent-audiences/" target="_blank"
                class="paragraph-link">Neurodivergent individuals often prefer softer shades</a> like pastels, greens,
              and blues. These soft shades have a calming effect that reduces sensory overload, a common issue for
              neuroatypical individuals.
            </p>
          </div>
        </div>

        <div class="column-87 w-col w-col-6">
          <div class="div-block-93">
            <p>
              Choosing the right imagery is another important aspect of inclusive design for a neuroatypical audience.
              Too many images, for example, can confuse your readers. As explained by a researcher from the University
              of Central Lancashire, the main point is to <a href="https://uxpamagazine.org/designing_children_adhd/"
                target="_blank" class="paragraph-link">create a calm environment to prevent overwhelming the reader</a>.
              For the same reason, it&#x27;s best to avoid placing text directly over an image. This is something you
              often find in other accessibility considerations, as well as SEO.
            </p>
          </div>
